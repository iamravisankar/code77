package mugesh012;

public class ArrayClass {
	String name;
	int age;
	boolean isMale;
	public ArrayClass(String name, int age, boolean isMale) {
		super();
		this.name = name;
		this.age = age;
		this.isMale = isMale;
	}
	@Override
	public String toString() {
		return "ArrayClass [name=" + name + ", age=" + age + ", isMale=" + isMale + "]";
	}
	

}
